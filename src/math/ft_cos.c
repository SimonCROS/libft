#include "ftmath.h"

double	ft_cos(double x)
{
	double	t;
	double	s;
	int		p;

	t = 1.0;
	s = 1.0;
	p = 0;
	while (ft_fabs(t / s) > .00000001)
	{
		p++;
		t = (-t * x * x) / ((2 * p - 1) * (2 * p));
		s += t;
	}
	return (s);
}
